for(let id in elaborateContents) {
    const noteData = elaborateContents[id];
    let contentString = 'content: ' + JSON.stringify(noteData, null, 6);
    
    // We want to replace everything inside the content: { ... } for this note block
    const regex = new RegExp(`(id:\\s*'${id}'[\\s\\S]*?isExternal:\\s*false,\\s*)content:\\s*\\{[\\s\\S]*?(?=\\n\\s*\\},\\n\\s*\\{|\\n\\s*\\}\\n*];?)`);
    
    const match = regex.exec(newCode);
    if(match) {
        newCode = newCode.replace(regex, `$1${contentString}`);
    } else {
        console.log(`Failed to match ${id}`);
    }
}

