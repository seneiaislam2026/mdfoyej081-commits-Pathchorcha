function parseQuestion(raw) {
  // raw format: "১. question \nক) opt1 খ) opt2 গ) opt3 ঘ) opt4 \nউত্তর: খ"
  const lines = raw.split("\n");
  const qStr = lines[0].replace(/^\d+[\.।]\s*/, "");
  
  const optsRaw = lines[1] || "";
  const matchK = optsRaw.match(/ক\)\s*(.*?)\s*(?=খ\)|$)/);
  const matchKh = optsRaw.match(/খ\)\s*(.*?)\s*(?=গ\)|$)/);
  const matchG = optsRaw.match(/গ\)\s*(.*?)\s*(?=ঘ\)|$)/);
  const matchGh = optsRaw.match(/ঘ\)\s*(.*)/);

  const k = matchK ? matchK[1].trim() : "";
  const kh = matchKh ? matchKh[1].trim() : "";
  const g = matchG ? matchG[1].trim() : "";
  const gh = matchGh ? matchGh[1].trim() : "";

  const options = [];
  if (k) options.push({ id: "ক", text: k });
  if (kh) options.push({ id: "খ", text: kh });
  if (g) options.push({ id: "গ", text: g });
  if (gh) options.push({ id: "ঘ", text: gh });

  const ansRaw = lines[2] || "";
  const matchAns = ansRaw.match(/উত্তর:\s*(ক|খ|গ|ঘ)/);
  const correctAnswer = matchAns ? matchAns[1] : "";

  return {
    text: qStr,
    options,
    correctOption: correctAnswer,
    classGroup: "Admission",
    subject: "ব্যবসায় সংগঠন",
    title: "ম্যানেজমেন্ট ১ম অধ্যায় — সেরা ৫০টি অতি গুরুত্বপূর্ণ এমসিকিউ"
  };
}

async function run() {
  const colRef = collection(db, "questions");
  for (const q of questions) {
    let parsed = parseQuestion(q);
    await addDoc(colRef, {
      ...parsed,
      createdAt: serverTimestamp()
    });
    console.log("Added:", parsed.text);
  }
  console.log("Done");
  process.exit();
}
run().catch(console.error);
