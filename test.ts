async function run() {
  const oldData = managementMCQs;

  console.log("Fetching existing...");
  const colRef = collection(db, "questions");
  const qTitle = "ম্যানেজমেন্ট ১ম অধ্যায় — সেরা ৫০টি অতি গুরুত্বপূর্ণ এমসিকিউ";
  const qObj = query(colRef, where("title", "==", qTitle));
  
  try {
    const snap = await getDocs(qObj);
    console.log("Found", snap.size, "to delete");
    
    // delete existing
    const batch1 = writeBatch(db);
    snap.docs.forEach(d => {
      batch1.delete(d.ref);
    });
    await batch1.commit();
    console.log("Deleted old docs.");
    
    // Update old data with explanation and insert directly to firestore
    const batch2 = writeBatch(db);
    for (const q of oldData) {
      const qNum = q.text.split(" ")[0]; // "১."
      const matchedExp = questionsWithExplanations.find(qe => qNum.startsWith(qe.q));
      const explanation = matchedExp ? matchedExp.exp : "সঠিক উত্তর: " + (q.options.find(o => o.id === q.correctOption)?.text || q.correctOption);
      
      // Using batch for better speed
      const newRef = doc(collection(db, "questions"));
      batch2.set(newRef, {
        ...q,
        explanation,
        createdAt: serverTimestamp()
      });
    }
    await batch2.commit();
    console.log("Done inserting 50 questions with explanations!");
  } catch (err) {
    console.error("Error updating:", err);
  } finally {
    process.exit();
  }
}

run().catch(console.error);
